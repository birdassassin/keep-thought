/**
 * ============================================================================
 * 示例 1: 什么是 Redux？
 * ============================================================================
 * 
 * 【核心概念】
 * Redux 是一个用于 JavaScript 应用的状态管理库。
 * 
 * 【为什么要用 Redux？】
 * 想象一个场景：
 * - 你的应用有多个组件需要共享数据
 * - 组件层级很深，传递数据需要一层层传递（prop drilling）
 * - 某个组件修改了数据，其他组件需要知道并更新
 * 
 * Redux 提供了一个"中央仓库"（Store），所有组件都可以：
 * 1. 从 Store 读取数据
 * 2. 向 Store 发送指令修改数据
 * 3. 订阅数据变化，自动更新
 * 
 * 【Redux 三大原则】
 * 1. 单一数据源：整个应用的 state 存储在一个对象中
 * 2. State 只读：只能通过发送 Action 来修改
 * 3. 使用纯函数修改：Reducer 必须是纯函数
 * 
 * 【工作流程】
 * 
 *    Component          Dispatch           Reducer           Store
 *       │                 │                  │                │
 *       │  1.用户点击按钮  │                  │                │
 *       ├────────────────>│                  │                │
 *       │                 │  2.发送 Action   │                │
 *       │                 ├─────────────────>│                │
 *       │                 │                  │ 3.计算新 State │
 *       │                 │                  ├───────────────>│
 *       │                 │                  │                │ 4.更新 State
 *       │                 │                  │                │────┐
 *       │                 │                  │                │    │
 *       │  5.通知组件更新  │                  │                │<───┘
 *       │<────────────────├──────────────────┤                │
 *       │                 │                  │                │
 * 
 * 【传统 Redux 的问题】
 * 1. 配置太复杂：需要安装很多中间件
 * 2. 样板代码太多：每个功能都要写很多重复代码
 * 3. 学习曲线陡峭：概念太多，新手难以上手
 * 
 * 【Redux Toolkit 来解决】
 * Redux Toolkit (RTK) 是官方推荐的工具集，它：
 * - 简化配置
 * - 减少样板代码
 * - 内置最佳实践
 * - 默认启用 Redux DevTools
 */

